class SwitchDemo
{
	public static void main(String[] args)
	{
		int x = 2;
		switch(x)
		{
			case 4:
				System.out.println("a");
				break;
			case 6:
				System.out.println("b");
				break;
			case 2:
				System.out.println("c");
				break;
			default:
				System.out.println("d");
				break;
		}
		System.out.println("Hello World!");
	}
}
